export class Piece {
  constructor(type, rotation = 0, x = 0, y = 0) {
    this.type = type;
    this.rotation = rotation;
    this.x = x;
    this.y = y;
  }

  blocks() {
    return PIECES[this.type].blocks[this.rotation];
  }

  absoluteBlocks() {
    return this.blocks().map(([dx, dy]) => [dx + this.x, dy + this.y]);
  }

  rotatedCW() {
    return new Piece(this.type, (this.rotation + 1) % 4, this.x, this.y);
  }

  rotatedCCW() {
    return new Piece(this.type, (this.rotation + 3) % 4, this.x, this.y);
  }
}
